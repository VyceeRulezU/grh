-- 10. FIX PROFILE ROLE CONSISTENCY
-- ============================================================================
DO $$ 
BEGIN 
    ALTER TABLE public.profiles DROP CONSTRAINT IF EXISTS profiles_role_check;
    ALTER TABLE public.profiles DROP CONSTRAINT IF EXISTS profiles_role_final_check;
    ALTER TABLE public.profiles DROP CONSTRAINT IF EXISTS profiles_role_standard_check;

    UPDATE public.profiles SET role = 'learner' WHERE LOWER(role) = 'learner';
    UPDATE public.profiles SET role = 'admin' WHERE LOWER(role) = 'admin';
    UPDATE public.profiles SET role = 'instructor' WHERE LOWER(role) = 'instructor';
    
    UPDATE public.profiles SET role = 'learner' 
    WHERE role NOT IN ('learner', 'instructor', 'admin');

    ALTER TABLE public.profiles ADD CONSTRAINT profiles_role_final_check 
      CHECK (role IN ('learner', 'instructor', 'admin'));
END $$;
