-- ─── 1. LIBRARY RESOURCES TABLE ─────────────────────────────────────────────
DO $$ 
BEGIN 
    IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname = 'public' AND tablename = 'resources') AND 
       NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname = 'public' AND tablename = 'library_resources') THEN
        ALTER TABLE public.resources RENAME TO library_resources;
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS public.library_resources (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    type TEXT DEFAULT 'PERL',
    category TEXT DEFAULT 'Governance',
    description TEXT,
    file_url TEXT,
    status TEXT DEFAULT 'Published',
    created_at TIMESTAMPTZ DEFAULT now()
);

DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME='library_resources' AND COLUMN_NAME='file_url') THEN
        ALTER TABLE public.library_resources ADD COLUMN file_url TEXT;
    END IF;
END $$;

-- ─── 2. BOOKS TABLE ─────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.books (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    summary TEXT,
    image_url TEXT,
    file_url TEXT,
    status TEXT DEFAULT 'Published',
    created_at TIMESTAMPTZ DEFAULT now()
);

-- ─── 3. WORKSHOPS TABLES ────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.workshops (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    date TEXT,
    time TEXT,
    host TEXT,
    format TEXT DEFAULT 'Virtual',
    status TEXT DEFAULT 'Upcoming',
    location TEXT,
    capacity INTEGER DEFAULT 50,
    created_at TIMESTAMPTZ DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.workshop_registrations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workshop_id BIGINT REFERENCES public.workshops(id) ON DELETE CASCADE,
    user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
    full_name TEXT,
    email TEXT,
    role TEXT,
    reason TEXT,
    registered_at TIMESTAMPTZ DEFAULT now(),
    UNIQUE(workshop_id, user_id)
);

-- ─── 4. USER NOTES TABLE (for learn player) ────────────────────────────────
DROP TABLE IF EXISTS public.user_notes CASCADE;
CREATE TABLE public.user_notes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    course_id BIGINT NOT NULL,
    module_id BIGINT NOT NULL,
    note_text TEXT,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now(),
    UNIQUE(user_id, module_id)
);

-- ─── 5. DISCUSSIONS TABLE (for learn player) ────────────────────────────────
DROP TABLE IF EXISTS public.discussions CASCADE;
CREATE TABLE public.discussions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    course_id BIGINT NOT NULL,
    module_id BIGINT NOT NULL,
    message TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- ─── 6. USER PROGRESS TABLE (for learn player) ─────────────────────────────
DROP TABLE IF EXISTS public.user_progress CASCADE;
CREATE TABLE public.user_progress (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    course_id BIGINT NOT NULL,
    module_id BIGINT NOT NULL,
    completed BOOLEAN DEFAULT false,
    watched_seconds INTEGER DEFAULT 0,
    status TEXT DEFAULT 'not-started',
    created_at TIMESTAMPTZ DEFAULT now(),
    UNIQUE(user_id, module_id)
);

